greet = "Hello"

# Accessing the first element of a string
print(greet[0]) # 'H'

# Slicing a string
print(greet[0:3]) # 'Hel'
print(greet[0:2]) # 'He'

# Addding two strings
name = "Jule";
print(greet + " " + name) # Hello Jule

# Uppercasing a string
print(name.upper()) # JULE

# Splitting a string
friends = "Fama,Oumar,Ibou"
print(friends.split(",")) # ['Fama', 'Oumar', 'Ibou']
